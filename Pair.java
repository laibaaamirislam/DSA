public class Pair {
    int key;
    int value;
    int prob;

    public Pair(int key, int value) {
        this.key = key;
        this.value = value;
        this.prob = 0;
    }
    public int getKey() {
        return key;
    }

    public int getValue() {
        return value;
    }

    public int getProb() {
        return prob;
    }
}
